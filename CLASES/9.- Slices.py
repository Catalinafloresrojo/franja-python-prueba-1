#slices / rebanadas

nombre = "francisco"

print(nombre[0])
print(nombre[3])

print(nombre[0:3]) # no contara el numero 3

print(nombre[:3]) #omitimos el 0, programa lo sabe

print(nombre[3:]) #no es necesario agregar el ultimo digito, el programa llegara hasta el final

print(nombre[1:7])

print(nombre[1:7:2]) #va dando salto de 2 en 2 

print(nombre[::]) #comienza desde 0 hasta el final, dando salto de 1 en 1 

print(nombre [1::3])

print(nombre[::-1]) #lo escribe al reves 



